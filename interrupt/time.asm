.MODEL SMALL
.STACK 100H
.DATA 
   MSG DB 'CURRENT TIME:$'
.CODE
   MAIN PROC
   MOV AX,@DATA
   MOV DS,AX
  
  MOV AH,09H
  LEA DX,MSG
  INT 21H
  
  MOV AH,2CH
  INT 21H
  
  MOV AL,CH
  CALL PRINTNUM
  
  MOV AL,CL
  CALL PRINTNUM
  
  MOV AL,DH
  CALL PRINTNUM
  
  MOV AH,4CH
  INT 21H
  MAIN ENDP
  
  PRINTNUM PROC
  MOV AH,0
  MOV BL,10
  DIV BL
MOV BL,AH
ADD AL,'0'

MOV DL,AL
MOV AH,02H
INT 21H

MOV AL,BL
ADD AL,'0'
MOV DL,AL
MOV AH,02H
INT 21H

MOV DL,':'
MOV AH,02H
INT 21H

RET
PRINTNUM ENDP
END MAIN

